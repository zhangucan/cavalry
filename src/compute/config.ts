import { saveJsonToDb } from '@uranplus/cavalry-utils'

function saveExportToDb() {
    const config = require('config')
    const moment = require('moment')
    const month = config.get('cavalry.month')

    const teamConfigObject = require('./config2')
    const teamConfigs = Object.keys(teamConfigObject)
        .map(key => teamConfigObject[key])
        .map(teamConfig => {
            delete teamConfig.timeRange
            delete teamConfig.path
            teamConfig.month = moment(month)
            return teamConfig
        })
    saveJsonToDb(teamConfigs, 'team_config', 'team_config')
}

if (require.main === module) {
    saveExportToDb()
}
